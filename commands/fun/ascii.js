module.exports = {
    data: new SlashCommandBuilder()
        .setName('ascii')
        .setDescription('Convertir du texte en art ASCII')
        .addStringOption(option =>
            option.setName('texte')
                .setDescription('Le texte ├á convertir')
                .setRequired(true)
                .setMaxLength(15)),
    cooldown: 3,
    async execute(interaction, client) {
        const texte = interaction.options.getString('texte').toUpperCase();
        const lines = ['', '', '', '', ''];

        for (const char of texte) {
            const letter = asciiLetters[char];
            if (letter) {
                for (let i = 0; i < 5; i++) {
                    lines[i] += letter[i] + ' ';
                }
            }
        }

        const result = lines.join('\n');

        if (result.length > 1900) {
            return interaction.reply({ content: 'ÔŁî Le texte est trop long pour ├¬tre affich├ę.', ephemeral: true });
        }

        const embed = new EmbedBuilder()
            .setColor('#3498DB')
            .setTitle('­čöĄ Art ASCII')
            .setDescription(`\`\`\`\n${result}\n\`\`\``)
            .setFooter({ text: `Demand├ę par ${interaction.user.username}`, iconURL: interaction.user.displayAvatarURL() })
            .setTimestamp();

        await interaction.reply({ embeds: [embed] });
    },
};
